// Generates the OpenUI Lang system prompt from the installed component library
// and writes it to src/openui/generated-system-prompt.ts as a static string.
//
// WHY A SEPARATE SCRIPT?
// openuiChatLibrary.prompt() loads the React component renderers from
// @openuidev/react-ui/genui-lib. That module cannot be evaluated inside the
// Next.js RSC/SSR or Mastra server graphs (it calls createContext at module
// eval time). Running it here, in a plain Node process, is safe — and emitting
// a static string keeps the server import 100% free of React components.
//
// Re-run after upgrading @openuidev/react-ui so the prompt matches the new
// component signatures. It runs automatically as a prebuild step.
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { openuiChatLibrary, openuiChatPromptOptions } from "@openuidev/react-ui/genui-lib";

const PREAMBLE =
  "You are Dishant Sharma's AI portfolio assistant. You answer questions about Dishant's work, projects, skills, and experience using rich UI components. Your entire response must be valid OpenUI Lang code only. Do not return markdown, prose, JSON, or explanations.";

const ADDITIONAL_RULES = [
  "Always fetch data via tools before generating UI - never invent placeholder data.",
  "Use charts for trends or comparisons when data supports it.",
  "Avoid generic 'Insights' sections unless the user asks.",
  "Always end with FollowUpBlock containing 2 specific follow-up questions grounded in the current response.",
  "FollowUp questions should point to data the UI can visualize (charts, tables, comparisons).",
  "Use TagBlock for technology badges, languages, and skills.",
  "Use SectionBlock for long detailed responses like project breakdowns or experience summaries.",
];

const prompt = openuiChatLibrary.prompt({
  ...openuiChatPromptOptions,
  preamble: PREAMBLE,
  additionalRules: [
    ...(openuiChatPromptOptions.additionalRules ?? []),
    ...ADDITIONAL_RULES,
  ],
});

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = resolve(__dirname, "../src/openui/generated-system-prompt.ts");

const fileContents = `// AUTOGENERATED by scripts/generate-openui-prompt.mjs — do not edit by hand.
// Regenerate after upgrading @openuidev/react-ui.
export const generatedOpenUISystemPrompt = ${JSON.stringify(prompt)};
`;

writeFileSync(outPath, fileContents, "utf8");
console.log(`Wrote OpenUI system prompt (${prompt.length} chars) to ${outPath}`);
